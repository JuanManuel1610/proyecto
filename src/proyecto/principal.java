    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        // TODO add your handling code here:
        archivos conexion = new archivos();
        atributos dato = new atributos();
        
        dato.setNombre(txtnombre.getText().toString());
        dato.setTelefono(txttelefono.getText().toString());
        dato.setCorreo(txtcorreo.getText().toString());
        dato.setProfesion(txtpro.getText().toString());
        
        conexion.agregar(dato);
        datos = conexion.getDatos();
        
        if(conexion.Grabar(datos)){
            
        JOptionPane.showMessageDialog(this, "Se grabo corectamente", "Grabado",JOptionPane.INFORMATION_MESSAGE);
        CargarContenidoTabla();
        }else{
        JOptionPane.showMessageDialog(this, "Error al grabar informacion", "Error",JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_btnagregarActionPerformed

