    private void Cargar(){
        archivos conexion = new archivos();
        if(conexion.verificaArch()){
            if(conexion.Leer()){
                datos = conexion.getDatos();
               CargarContenidoTabla();
            }else{
                JOptionPane.showMessageDialog(this, "nose puede leer la informacion", "Error",JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "No existe", "Error",JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void TitulosTabla(){
        modelotabla.addColumn("nombre");
        modelotabla.addColumn("telefono");
        modelotabla.addColumn("correo");
        modelotabla.addColumn("profesion");
        table.setModel(modelotabla);
    }
    
    private void CargarContenidoTabla(){
        Object[] filatabla = new Object[4];
        for(atributos dato : datos){
            filatabla[0]=dato.getNombre();
            filatabla[1]=dato.getTelefono();
            filatabla[2]=dato.getCorreo();
            filatabla[3]=dato.getProfesion();
            modelotabla.addRow(filatabla);
        }
        table.setModel(modelotabla);
    }

